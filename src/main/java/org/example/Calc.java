package org.example;

public class Calc {
    public int addition(int a, int b) {
        int result;
        result = a + b;
        System.out.println(a + " + " + b + " = " + result);
        return result;
    }

    public int subtraction(int a, int b) {
        int result;
        result = a - b;
        System.out.println(a + " - " + b + " = " + result);
        return result;
    }

}
